export type LanguageCode = keyof typeof translations;

export function getTranslation(language: string, key: string): string {
  const code = (language in translations ? language : 'en') as LanguageCode;
  
  if (translations[code] && key in translations[code]) {
    return (translations[code] as any)[key];
  }
  
  const dict = (newTranslations as any)[code] || newTranslations.en;
  if (dict && key in dict) {
    return dict[key];
  }
  
  if (key in translations.en) {
    return (translations.en as any)[key];
  }
  
  if (key in newTranslations.en) {
    return (newTranslations.en as any)[key];
  }
  
  return String(key);
}